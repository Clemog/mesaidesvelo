	<h1>Les aides à l’achat de vélo à Bordeaux</h1>

	<p>
		Acheter un nouveau vélo peut être un investissement important. Heureusement, vous pouvez
		peut-être bénéficier d'<strong>aides financières pour l'achat d'un vélo à Bordeaux</strong>. En
		effet l'État et Bordeaux Métropole cherchent à encourager l'usage du vélo car c'est un moyen de
		transport écologique et bon pour la santé.
	</p>

	<p>Le montant des aides dépend de plusieurs critères, en particulier :</p>
	<ul>
		<li>le type de vélo acheté</li>
		<li>le prix du vélo</li>
		<li>les revenus de votre foyer</li>
	</ul>
	<p>
		Pour vous aider à y voir plus clair, nous proposons un calculateur d'aide simple et gratuit. Le
		tableau ci-dessus indique le montant maximum auquel vous pouvez prétendre pour l'achat de chaque
		type de vélo. Cliquez sur une catégorie pour accéder au calculateur détaillé et personnalisez le
		résultat en quelques clics.
	</p>

	<h2>Les aides de Bordeaux Métropole</h2>
	<img
		src="/miniatures/logo_intercommunalite_bordeaux.webp"
		alt="Logo de Bordeaux Métropole"
		width="140"
		class="float-left pt-4 mr-6"
	/>
	<p>
		Bordeaux Métropole renouvelle jusqu'en 2023 le dispositif d'aide à l'acquisition de vélos à
		assistance électrique, vélos pliants, vélos-cargo, tricycles pour adultes et dispositifs
		d'électrification de vélos standards, en faveur des résidents de la métropole et de salariés
		d'entreprises situées sur le territoire métropolitain ayant adopté un plan de déplacements
		entreprise.
	</p>
	<p>
		Le montant de l’aide est fixé à 100€ maximum par vélo. Une personne éligible ne peut recevoir
		qu’une aide par an. Dans le cas où le prix d’achat est inférieur à 100 €, le montant de l’aide
		est équivalent au prix d’achat du matériel.
	</p>

	<h2>Le bonus écologique de l’État</h2>

	<p>Pour bénéficier du bonus écologique de l’État vous devez remplir les conditions suivantes :</p>
	<ul>
		<li>Être majeur et domicilié en France</li>
		<li>
			Avoir un revenu fiscal de référence par part de l'année précédant l'achat du vélo est
			inférieur ou égal à 13 489 €
		</li>
	</ul>
	<p>
		L’aide est de <strong>40% du prix du vélo</strong>, et elle est plafonnée en fonction du type de
		vélo. Par ailleurs depuis le 15 août 2022, les ménages les plus modestes bénéficient de plafond
		plus élevés ainsi que d’une aide spécifique pour les vélos mécaniques non électriques.
	</p>

