<div class="prose my-12">
	<h2>Qu’est ce que le rétrofit ?</h2>
	<p>
		Le rétrofit consiste à remplacer le moteur thermique d’une voiture par un moteur électrique et
		une batterie.
	</p>
	<p>
		C’est une alternative à l’achat d’une voiture électrique neuve pour réduire les émissions de
		CO2.
	</p>

	<h2>Combien ça coûte ?</h2>
	<p>
		Le prix d’un rétrofit électrique varie en fonction du véhicule et de la batterie installée. Il
		débute à 5&nbsp;000&nbsp;€ pour une petite citadinne et peut monter à 20&nbsp;000&nbsp;€ pour
		les plus gros modèles.
	</p>
	<p>Un rétrofit est théoriquement moins cher que l’achat d’un véhicule neuf équivalent.</p>

	<h2>Quelles sont les aides ?</h2>
	<p>
		L’État, ainsi que certaines régions et métropoles subventionnent le rétrofit d’une voiture pour
		les particuliers.
	</p>
	<p>
		Pour découvrir les aides auxquelles vous avez le droit, saisissez simplement votre ville dans la
		barre de recherche en haut de la page !
	</p>
</div>
